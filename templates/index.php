<h1>LangRoot</h1>

<p>Cherchez un mot dans le champs en haut à droite de l'écran. S'il existe, une
completion automatique vous sera proposée.</p>

<p>Si un mot n'a pas de racines renseignées, cliquer sur « Éditer » permet
d'éditer le mot.</p>

<p>Les « concepts » du mots sont ses racines, les racines de ses racines, et
ainsi de suite.</p>

<p>L'onglet « Tout » permet de voir les mots qui ont déjà été saisis. Vous
pouvez bien sûr les modifier.</p>

<p>Cliquer sur un mot vous envoie sur la page « Voir » de ce mot.</p>

<p>Très peu de caractères sont autorisés. Les lettres non-accentuées, les
nombres, et les caractères « _ » et « - ». Vu l'usage, ce n'est pas bien grave,
et ça simplifie énormément le système. Désolé.</p>

<p>Vous pouvez supprimer un mot à partir de l'onglet « Tout ». <big
class="text-danger bg-danger">Aucune confirmation n'est demandée</big>.
Supprimer un mot qui est référencé par d'autre ne va supprimer que ses racines.
Il sera « régénéré » par les mots qu'il définit.</p>
